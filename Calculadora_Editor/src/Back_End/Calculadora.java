public class Calculadora {
    
    
    public int soma (int a, int b){
        return a+b;
    }
    public int subt (int a, int b){
        return a-b;
    }
    public int mult (int a, int b){
        return a*b;
    }
    public int divi (int a, int b){
        return a/b;
    }
}

